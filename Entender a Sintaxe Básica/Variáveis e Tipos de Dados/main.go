package main

import "fmt"

func main() {
	// Declaração de variáveis
	var inteiro int
	var flutuante float64
	var texto string
	var booleano bool

	// Atribuindo valores às variáveis
	inteiro = 10
	flutuante = 3.14
	texto = "Olá, mundo!"
	booleano = true

	// Imprimindo os valores das variáveis
	fmt.Println("Inteiro:", inteiro)
	fmt.Println("Flutuante:", flutuante)
	fmt.Println("Texto:", texto)
	fmt.Println("Booleano:", booleano)
}
